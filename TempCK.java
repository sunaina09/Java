class TempCK{
//27. Write a program in java to convert temperature in Celsius to Kelvin.   

 public static void main(String []args){
   double tempC=26.85, tempK=(tempC+273.15);
   System.out.println("The temperature in Celsius : "+tempC);
   System.out.println("The temperature in Kelvin : "+tempK);
 }
}