class QnA17{
//17. Write a program in C++ to find the Area and Perimeter of a Rectangle.   

  public static void main(String [] args){
    int l=10,w=15;
    System.out.println("Input the length of the rectangle :"+l);
    System.out.println("Input the width of the rectangle : "+w);
    System.out.println("The area of the rectangle is :"+(l*w));
    System.out.println("The perimeter of the rectangle is :"+(2*(l+w)));
  }
}