class QnA20{
//20. Write a program in C++ to convert temperature in Celsius to Fahrenheit.   
  public static void main(String []args){
    int tempC=35;
    System.out.println("The temperature in Celsius :"+tempC);
    int tempF=((9*tempC)/5+32);
    System.out.println("The temperature in Fahrenheit : "+tempF);
    }
}
