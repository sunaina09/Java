class Temp1{
//21. Write a program in java to convert temperature in Fahrenheit to Celsius.   

 public static void main(String[] args){
   double temf=95;
   double tempc=(5*(temf-32))/9;
   System.out.println("The temperature in Fahrenheit :"+temf );
   System.out.println("The temperature in Celsius :"+ tempc);
 }
}