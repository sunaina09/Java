class QnA16{
//16. Write a program in C++ to calculate the volume of a cylinder.   

  public static void main(String [] args){
    double pi=3.14159;
    int r=6, h=8;
    System.out.println("Input the radius of a cylinder :"+r);
    System.out.println("Input the height of a cylinder :"+h);
    System.out.println("The volume of a cylinder is : "+(pi*(r*r*h)));
  }
}