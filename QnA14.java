class QnA14 {

//14. Write a program in C++ to calculate the volume of a sphere.   

 public static void main(String []args){
    final double pi=3.14159;
    int r=6;
    System.out.println("Input the radius of a sphere :"+r);
    System.out.println("The volume of a sphere is : "+((4*pi*(r*r*r))/3));
 }
}