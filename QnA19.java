class QnA19{
//19. Write a program in C++ to find the area and circumference of a circle.   
  public static void main(String []args){
    double r=5, pi=3.14159;
    System.out.println("Input the radius(1/2 of diameter) of a circle :"+r);
    System.out.println("The area of the circle is :"+(pi*r*r));
    System.out.println("The circumference of the circle is :"+(2*pi*r));
  }
}